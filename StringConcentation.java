package JavaBasic;

public class StringConcentation {

	public static void main(String[] args) {
		int i = 50;
		int j = 60;
		
		String x = "Hi";
		String y = "World";
		System.out.println(i+j);
		System.out.println(x+y);
		
		System.out.println(i+j+x+y);
		System.out.println(x+y+i+j);
		System.out.println(x+y+(i+j));
		
				
		
		

	}

}
